package com.lesports.stadium.bean;

public class MyPrizeDetial {
	//奖品 简介
	private String description;
	//图片url
	private String detailedImage;
	//详细备注
	private String detailedRemark;
	//图文详情url
	private String infoUrl;
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDetailedImage() {
		return detailedImage;
	}
	public void setDetailedImage(String detailedImage) {
		this.detailedImage = detailedImage;
	}
	public String getDetailedRemark() {
		return detailedRemark;
	}
	public void setDetailedRemark(String detailedRemark) {
		this.detailedRemark = detailedRemark;
	}
	public String getInfoUrl() {
		return infoUrl;
	}
	public void setInfoUrl(String infoUrl) {
		this.infoUrl = infoUrl;
	}
	

}
