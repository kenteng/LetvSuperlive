package com.lesports.stadium.bean;

public class CreateOrderBean {
	private String amount;
	private String id;
	private String integralCount;
	private String integralRechargeId;
	private String orderNumber;
	private String payType;
	private String remark;
	private String userId;
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIntegralCount() {
		return integralCount;
	}
	public void setIntegralCount(String integralCount) {
		this.integralCount = integralCount;
	}
	public String getIntegralRechargeId() {
		return integralRechargeId;
	}
	public void setIntegralRechargeId(String integralRechargeId) {
		this.integralRechargeId = integralRechargeId;
	}
	public String getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(String orderNumber) {
		this.orderNumber = orderNumber;
	}
	public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
}
