package com.lesports.stadium.bean;

public class IntegralRuleBean {
	private String createTime;
	private String id;
	private String integralRule;
	private String integralRuleName;
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIntegralRule() {
		return integralRule;
	}
	public void setIntegralRule(String integralRule) {
		this.integralRule = integralRule;
	}
	public String getIntegralRuleName() {
		return integralRuleName;
	}
	public void setIntegralRuleName(String integralRuleName) {
		this.integralRuleName = integralRuleName;
	}
	
}
